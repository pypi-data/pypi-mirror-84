/* Tests the libfsapfs_file_system_btree_initialize function
 * Returns 1 if successful or 0 if not
 */
int fsapfs_test_file_system_btree_initialize(
     void )
{
	libcerror_error_t *error                         = NULL;
	libfsapfs_file_system_btree_t *file_system_btree = NULL;
	int result                                       = 0;

#if defined( HAVE_FSAPFS_TEST_MEMORY )
	int number_of_malloc_fail_tests                  = 1;
	int number_of_memset_fail_tests                  = 1;
	int test_number                                  = 0;
#endif

	/* Test regular cases
	 */
	result = libfsapfs_file_system_btree_initialize(
	          &file_system_btree,
	          NULL,
	          NULL,
	          NULL,
	          NULL,
	          0,
	          0,
	          &error );

	FSAPFS_TEST_ASSERT_EQUAL_INT(
	 "result",
	 result,
	 1 );

	FSAPFS_TEST_ASSERT_IS_NOT_NULL(
	 "file_system_btree",
	 file_system_btree );

	FSAPFS_TEST_ASSERT_IS_NULL(
	 "error",
	 error );

	result = libfsapfs_file_system_btree_free(
	          &file_system_btree,
	          &error );

	FSAPFS_TEST_ASSERT_EQUAL_INT(
	 "result",
	 result,
	 1 );

	FSAPFS_TEST_ASSERT_IS_NULL(
	 "file_system_btree",
	 file_system_btree );

	FSAPFS_TEST_ASSERT_IS_NULL(
	 "error",
	 error );

	/* Test error cases
	 */
	result = libfsapfs_file_system_btree_initialize(
	          NULL,
	          NULL,
	          NULL,
	          NULL,
	          NULL,
	          0,
	          0,
	          &error );

	FSAPFS_TEST_ASSERT_EQUAL_INT(
	 "result",
	 result,
	 -1 );

	FSAPFS_TEST_ASSERT_IS_NOT_NULL(
	 "error",
	 error );

	libcerror_error_free(
	 &error );

	file_system_btree = (libfsapfs_file_system_btree_t *) 0x12345678UL;

	result = libfsapfs_file_system_btree_initialize(
	          &file_system_btree,
	          NULL,
	          NULL,
	          NULL,
	          NULL,
	          0,
	          0,
	          &error );

	file_system_btree = NULL;

	FSAPFS_TEST_ASSERT_EQUAL_INT(
	 "result",
	 result,
	 -1 );

	FSAPFS_TEST_ASSERT_IS_NOT_NULL(
	 "error",
	 error );

	libcerror_error_free(
	 &error );

#if defined( HAVE_FSAPFS_TEST_MEMORY )

	for( test_number = 0;
	     test_number < number_of_malloc_fail_tests;
	     test_number++ )
	{
		/* Test libfsapfs_file_system_btree_initialize with malloc failing
		 */
		fsapfs_test_malloc_attempts_before_fail = test_number;

		result = libfsapfs_file_system_btree_initialize(
		          &file_system_btree,
		          NULL,
		          NULL,
		          NULL,
		          NULL,
		          0,
		          0,
		          &error );

		if( fsapfs_test_malloc_attempts_before_fail != -1 )
		{
			fsapfs_test_malloc_attempts_before_fail = -1;

			if( file_system_btree != NULL )
			{
				libfsapfs_file_system_btree_free(
				 &file_system_btree,
				 NULL );
			}
		}
		else
		{
			FSAPFS_TEST_ASSERT_EQUAL_INT(
			 "result",
			 result,
			 -1 );

			FSAPFS_TEST_ASSERT_IS_NULL(
			 "file_system_btree",
			 file_system_btree );

			FSAPFS_TEST_ASSERT_IS_NOT_NULL(
			 "error",
			 error );

			libcerror_error_free(
			 &error );
		}
	}
	for( test_number = 0;
	     test_number < number_of_memset_fail_tests;
	     test_number++ )
	{
		/* Test libfsapfs_file_system_btree_initialize with memset failing
		 */
		fsapfs_test_memset_attempts_before_fail = test_number;

		result = libfsapfs_file_system_btree_initialize(
		          &file_system_btree,
		          NULL,
		          NULL,
		          NULL,
		          NULL,
		          0,
		          0,
		          &error );

		if( fsapfs_test_memset_attempts_before_fail != -1 )
		{
			fsapfs_test_memset_attempts_before_fail = -1;

			if( file_system_btree != NULL )
			{
				libfsapfs_file_system_btree_free(
				 &file_system_btree,
				 NULL );
			}
		}
		else
		{
			FSAPFS_TEST_ASSERT_EQUAL_INT(
			 "result",
			 result,
			 -1 );

			FSAPFS_TEST_ASSERT_IS_NULL(
			 "file_system_btree",
			 file_system_btree );

			FSAPFS_TEST_ASSERT_IS_NOT_NULL(
			 "error",
			 error );

			libcerror_error_free(
			 &error );
		}
	}
#endif /* defined( HAVE_FSAPFS_TEST_MEMORY ) */

	return( 1 );

on_error:
	if( error != NULL )
	{
		libcerror_error_free(
		 &error );
	}
	if( file_system_btree != NULL )
	{
		libfsapfs_file_system_btree_free(
		 &file_system_btree,
		 NULL );
	}
	return( 0 );
}

